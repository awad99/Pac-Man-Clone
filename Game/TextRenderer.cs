        public static void DrawText(SpriteBatch spriteBatch, string text, int x, int y, Color color, int scale = 1)
        {
            if (string.IsNullOrEmpty(text)) return;

            int currentX = x;
            foreach (char c in text.ToUpper())
            {
                if (_font.TryGetValue(c, out byte[] charData))
                {
                    for (int row = 0; row < 8; row++)
                    {
                        byte rowByte = charData[row];
                        for (int col = 0; col < 8; col++)
                        {
                            // Bits are ordered from MSB to LSB left-to-right (bit 7 is col 0, bit 0 is col 7)
                            int shift = 7 - col;
                            if (((rowByte >> shift) & 1) == 1)
                            {
                                spriteBatch.Draw(_pixel, 
                                    new Rectangle(currentX + col * scale, y + row * scale, scale, scale), 
                                    color);
                            }
                        }
                    }
                }
                currentX += 8 * scale; // Move to next character position
            }
        }
